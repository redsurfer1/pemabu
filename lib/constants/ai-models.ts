/**
 * Validated Anthropic model ID constants.
 *
 * PRIMARY  — Claude Sonnet 4 (balanced: quality + cost for structured outputs
 *            and multi-section documents; default for Strategy Council memo).
 * FAST     — Claude Haiku 4.5 (low-latency, low-cost; for short narrative
 *            generation, single-holding explanations, and governance summaries
 *            where brevity is more important than depth).
 *
 * NEVER use raw model ID strings outside this file.  If Anthropic renames a
 * model, update here and the entire codebase follows.
 *
 * STRATEGY_COUNCIL_MODEL is not defined here because it may be overridden at
 * runtime via STRATEGY_COUNCIL_ANTHROPIC_MODEL env var (see lib/services/ai.ts).
 */
export const AI_MODELS = {
  /** Claude Sonnet 4 — primary quality tier */
  PRIMARY: "claude-sonnet-4-20250514",
  /** Claude Haiku 4.5 — fast / cost-optimised tier */
  FAST: "claude-haiku-4-5-20251001",
} as const;

export type AiModelId = (typeof AI_MODELS)[keyof typeof AI_MODELS];

