/**
 * Startup validation — call once from the app entry point (e.g. next.config.ts
 * instrumentation hook or the first server action that uses AI) to surface
 * misconfiguration early rather than at the first real API call.
 *
 * Throws if ANTHROPIC_API_KEY is absent.
 * Warns if STRATEGY_COUNCIL_ANTHROPIC_MODEL is set to an unrecognised value.
 */
export function validateAnthropicConfig(): void {
  if (!process.env.ANTHROPIC_API_KEY?.trim()) {
    throw new Error(
      "[ai-models] ANTHROPIC_API_KEY is not set. All AI features will fail at runtime. " +
      "Set it in .env.local (development) or the deployment environment (production).",
    );
  }

  const councilModel = process.env.STRATEGY_COUNCIL_ANTHROPIC_MODEL?.trim();
  if (councilModel && !councilModel.startsWith("claude-")) {
    console.warn(
      `[ai-models] STRATEGY_COUNCIL_ANTHROPIC_MODEL="${councilModel}" does not look like a ` +
      `valid Anthropic model ID (expected to start with "claude-"). Check for typos.`,
    );
  }
}
